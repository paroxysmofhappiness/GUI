function GUIDB.Cache()
	GUI.Write("GUIDB " .. GUIDB.Version)
	
	for k, v in pairs(GUIDB.Additionally) do
		GUIDB.LoadImage(k, "", v)
	end
	
	if (Menu.IsEnabled(GUI.PreCacheItems)) then
		for k, v in pairs(GUIDB.Items) do
			GUIDB.LoadImage(k:gsub("item_", ""), GUIDB.IconPath)
		end
	end
	
	if (Menu.IsEnabled(GUI.PreCacheHeroes)) then
		for k, v in pairs(GUIDB.Heroes) do
			local tempName = k:gsub("npc_dota_hero_", "")
			
			if tempName == "pangolier" or tempName == "dark_willow" then
				GUIDB.LoadImage(tempName, "", "~/Extra/" .. tempName)
			else 
				GUIDB.LoadImage(tempName, GUIDB.HeroPath)
			end
			
			GUIDB.LoadImage('mini_' .. tempName, "", GUIDB.MiniHeroPath .. tempName)
		end
	end
	
	if (Menu.IsEnabled(GUI.PreCacheAb)) then
		for k, v in pairs(GUIDB.Abilities) do
			GUIDB.LoadImage(k, "", GUIDB.SpellPath .. v)
		end
	end
	
	GUI.Write("GUIDB cached [" .. GUIDB.TotalLoad .. "]")
end

function GUIDB.LoadImage(k, path, name)

	if (GUIDB.Images[k] == nil) then
		if string.match((name or path .. k), "grimstroke") and string.match((name or path .. k), "mini") then
			GUIDB.Images[k]	= Renderer.LoadImage('panorama/images/heroes/icons/npc_dota_hero_grimstroke_png.vtex_c')
		else
			GUIDB.Images[k]	= Renderer.LoadImage((name or path .. k) .. ".png")
		end
		GUIDB.TotalLoad	= GUIDB.TotalLoad + 1
	end
end

function GUIDB.SearchValue(k)
	if (GUIDB.Items["item_" .. k] ~= nil or GUIDB.Items[k] ~= nil) then
		GUIDB.LoadImage(k:gsub("item_", ""), GUIDB.IconPath)
		return true
	end
	
	if (GUIDB.Abilities[k] ~= nil) then
		GUIDB.LoadImage(k, "", GUIDB.SpellPath .. GUIDB.Abilities[k])
		return true
	end

	if (GUIDB.Heroes[k] ~= nil or GUIDB.Heroes[k:gsub("mini_", "npc_dota_hero_")] or GUIDB.Heroes["npc_dota_hero_" .. k]) then
		local tempName = k:gsub("npc_dota_hero_", "")
		tempName = tempName:gsub("mini_", "")
		
		if tempName == "pangolier" or tempName == "dark_willow" then
			GUIDB.LoadImage(tempName, "", "~/Extra/" .. tempName)
		else 
			GUIDB.LoadImage(tempName, GUIDB.HeroPath)
		end
		GUIDB.LoadImage('mini_' .. tempName, "", GUIDB.MiniHeroPath .. tempName)
		return true
	end

	return false
end

function GUIDB.Image(name)
	if string.match(name, "recipe_") then name = "recipe" end
	
	name = name:gsub("item_", "")
	name = name:gsub("npc_dota_hero_", "")
	
	if (GUIDB.Images[name] == nil) then
		if	not GUIDB.SearchValue(name) then
			name = "neutral"
		end
	end
	return GUIDB.Images[name]
end

